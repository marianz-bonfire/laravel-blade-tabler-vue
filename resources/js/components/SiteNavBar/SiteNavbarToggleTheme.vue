<template>
    <div class="d-none d-md-flex">
        <a @click="toggleTheme" class="nav-link px-0 hide-theme-dark" title="Enable dark mode" data-bs-toggle="tooltip"
            data-bs-placement="bottom">
            <!-- Download SVG icon from http://tabler-icons.io/i/moon -->
            <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none"
                stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="icon">
                <path stroke="none" d="M0 0h24v24H0z" fill="none" />
                <path d="M12 3c.132 0 .263 0 .393 0a7.5 7.5 0 0 0 7.92 12.446a9 9 0 1 1 -8.313 -12.454z" />
            </svg>
        </a>
        <a @click="toggleTheme" class="nav-link px-0 hide-theme-light" title="Enable light mode"
            data-bs-toggle="tooltip" data-bs-placement="bottom">
            <!-- Download SVG icon from http://tabler-icons.io/i/sun -->
            <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none"
                stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="icon">
                <path stroke="none" d="M0 0h24v24H0z" fill="none" />
                <path d="M12 12m-4 0a4 4 0 1 0 8 0a4 4 0 1 0 -8 0" />
                <path d="M3 12h1m8 -9v1m8 8h1m-9 8v1m-6.4 -15.4l.7 .7m12.1 -.7l-.7 .7m0 11.4l.7 .7m-12.1 -.7l-.7 .7" />
            </svg>
        </a>
    </div>

</template>

<script>


export default {
    name: "site-navbar-toggle-theme",
    components: {
    },
    data() {
        return {
            theme: 'light',
            toggleEye: false
        }
    },
    mounted() {
        let storedTheme = localStorage.getItem('theme');
        if (storedTheme) {
            this.theme = storedTheme;
            if (this.theme == 'dark') {
                document.body.setAttribute("data-bs-theme", this.theme);
            }
        }
    },
    methods: {
        toggleTheme() {
            this.theme = this.theme == 'light' ? 'dark' : 'light';
            localStorage.setItem('theme', this.theme);
            if (this.theme == 'dark') {
                document.body.setAttribute("data-bs-theme", this.theme);
            } else {
                document.body.removeAttribute("data-bs-theme");
            }
        }
    },
}
</script>

